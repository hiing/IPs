export function t(locale: Locale, key: string): string {
    return translations[locale]?.[key] ?? translations.en[key] ?? key;
}

export function tf(locale: Locale, key: string, vars: Record<string, string>): string {
    const template = t(locale, key);
    return template.replace(/\{(\w+)\}/g, (_, name: string) => vars[name] ?? `{${name}}`);
}

export function detectLocale(): Locale {
    if (typeof window === "undefined") return "en";

    // Check localStorage first
    const saved = localStorage.getItem("ip-insight-locale");
    if (saved && translations[saved as Locale]) return saved as Locale;

    // Auto-detect from browser
    const browserLang = navigator.language?.toLowerCase() || "";
    if (browserLang.startsWith("zh")) return "zh";
    if (browserLang.startsWith("ja")) return "ja";
    if (browserLang.startsWith("ko")) return "ko";
    if (browserLang.startsWith("es")) return "es";
    if (browserLang.startsWith("fr")) return "fr";
    if (browserLang.startsWith("de")) return "de";
    if (browserLang.startsWith("ru")) return "ru";
    return "en";
}
